package lib.ijoic.image_tool.color;

/**
 * Color config.
 *
 * @author VerstSiu verstsiu@126.com
 * @date 2017/10/6 14:27
 * @version 1.0
 */
class ColorConfig {

  private String suffix;
  private int color;

  /**
   * Returns suffix.
   */
  public String getSuffix() {
    return this.suffix;
  }

  /**
   * Set suffix.
   *
   * @param suffix suffix.
   */
  public void setSuffix(String suffix) {
    this.suffix = suffix;
  }

  /**
   * Returns color.
   */
  public int getColor() {
    return this.color;
  }

  /**
   * Set color.
   *
   * @param color color.
   */
  public void setColor(int color) {
    this.color = color;
  }

}
